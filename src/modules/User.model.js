import mongoose from "mongoose";

const Schema = new mongoose.Schema(
  {
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    firstName: {
      type: String,
      required: true,
    },
    lastName: {
      type: String,
      required: true,
    },
    adress: {
      type: String,
      required: true,
    },
    age: {
      type: Number,
      required: true,
    },
    phone: {
      type: Number,
      required: true,
    },
    avatar:{
      type:String,
      required: true
    }

  },
  {
    timestamps: true,
  },
);

export const UserModel = mongoose.model("Users", Schema);